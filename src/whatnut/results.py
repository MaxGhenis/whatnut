"""Paper results module - loads GENERATED results from data/results.json.

This replaces paper_results.py. All base-case values are generated by:
    python -m whatnut.pipeline --generate

The paper uses MyST's {eval} role to pull values directly from this module,
e.g., {eval}`r.walnut.qaly`.

Values are generated from code (not hardcoded), ensuring reproducibility.
The JSON file is committed to git as the reproducibility checkpoint.

Exception: `table_7_sensitivity()` runs the analysis live against
alternative confounding priors at doc-build time rather than reading
pre-computed values, so the sensitivity table always stays in sync with
the current model. This is the only result that is not materialized in
results.json.
"""

import json
from dataclasses import dataclass, field
from pathlib import Path


RESULTS_PATH = Path(__file__).parent / "data" / "results.json"


@dataclass
class NutResult:
    """Results for a single nut type."""

    name: str
    evidence: str
    qaly_mean: float
    qaly_ci_lower: float
    qaly_ci_upper: float
    qaly_undiscounted_mean: float
    p_positive: float
    p_negative: float
    life_years: float
    life_years_ci_lower: float
    life_years_ci_upper: float
    icer: float
    icer_ci_lower: float
    icer_ci_upper: float | None
    annual_cost: float
    rr_cvd: float
    rr_cancer: float
    rr_other: float
    cvd_contribution: float
    cancer_contribution: float
    other_contribution: float

    @property
    def qaly(self) -> str:
        return f"{self.qaly_mean:.2f}"

    @property
    def qaly_ci(self) -> str:
        return f"[{self.qaly_ci_lower:.2f}, {self.qaly_ci_upper:.2f}]"

    @property
    def icer_fmt(self) -> str:
        return f"${self.icer:,.0f}"

    @property
    def icer_ci_fmt(self) -> str:
        if self.icer_ci_upper is None:
            return f"[${self.icer_ci_lower:,.0f}, undefined]"
        return f"[${self.icer_ci_lower:,.0f}, ${self.icer_ci_upper:,.0f}]"

    @property
    def life_years_fmt(self) -> str:
        return f"{self.life_years:.2f}"

    @property
    def months(self) -> float:
        return self.life_years * 12

    @property
    def months_fmt(self) -> str:
        return f"{self.months:.1f}"

    @property
    def qaly_undiscounted(self) -> float:
        return self.qaly_undiscounted_mean

    @property
    def qaly_undiscounted_fmt(self) -> str:
        return f"{self.qaly_undiscounted:.2f}"


@dataclass
class PathwayRR:
    """Pathway-specific relative risks for a nut."""

    name: str
    cvd: float
    cancer: float
    other: float


@dataclass
class PaperResults:
    """All results for the paper, loaded from generated JSON.

    Every numerical value is sourced from `data/results.json`, which is
    regenerated by `python -m whatnut.pipeline --generate`. Nothing here
    should hold a load-bearing default — the JSON is the single source of
    truth.
    """

    # Model parameters
    seed: int
    n_samples: int
    start_age: int
    qaly_discount_rate: float
    cost_discount_rate: float

    # Confounding prior (alpha/beta/mean from JSON; CI derived from Beta ppf)
    confounding_alpha: float
    confounding_beta: float
    confounding_mean: float
    confounding_ci_lower: float
    confounding_ci_upper: float

    # Baseline metrics (derived from mortality + quality tables)
    baseline_life_years: float
    baseline_qalys: float
    average_quality_weight: float

    # E-value for the reference HR (Aune 2016 all-cause, HR=0.78)
    e_value: float

    # Pathway contributions (integer percent)
    cvd_contribution: int
    other_contribution: int
    cancer_contribution: int

    # Narrative constants loaded from data/constants.yaml via the JSON pipe.
    # These are integers/floats exposed to the paper text via r.target_age
    # etc., so they must be populated by _load_results (not defaulted here).
    target_age: int = field(default=40)
    life_expectancy: int = field(default=40)
    allergy_prevalence_lower: float = field(default=2.0)
    allergy_prevalence_upper: float = field(default=4.0)
    nice_lower_gbp: int = field(default=25000)
    nice_upper_gbp: int = field(default=35000)
    gbp_usd_rate: float = field(default=1.34)
    icer_threshold: int = field(default=50000)

    # Nut results (populated from JSON)
    nuts: dict[str, NutResult] = field(default_factory=dict)
    pathway_rrs: dict[str, PathwayRR] = field(default_factory=dict)

    # Convenience accessors
    @property
    def walnut(self) -> NutResult:
        return self.nuts["walnut"]

    @property
    def almond(self) -> NutResult:
        return self.nuts["almond"]

    @property
    def peanut(self) -> NutResult:
        return self.nuts["peanut"]

    @property
    def pecan(self) -> NutResult:
        return self.nuts["pecan"]

    @property
    def macadamia(self) -> NutResult:
        return self.nuts["macadamia"]

    @property
    def cashew(self) -> NutResult:
        return self.nuts["cashew"]

    @property
    def pistachio(self) -> NutResult:
        return self.nuts["pistachio"]

    @property
    def hazelnut(self) -> NutResult:
        return self.nuts["hazelnut"]

    # Derived ranges
    @property
    def qaly_range(self) -> str:
        vals = [n.qaly_mean for n in self.nuts.values()]
        return f"{min(vals):.2f}-{max(vals):.2f}"

    @property
    def qaly_undiscounted_range(self) -> str:
        vals = [n.qaly_undiscounted for n in self.nuts.values()]
        return f"{min(vals):.2f}-{max(vals):.2f}"

    @property
    def icer_range(self) -> str:
        vals = [n.icer for n in self.nuts.values()]
        return f"${min(vals):,.0f}-${max(vals):,.0f}"

    @property
    def nice_lower_usd(self) -> int:
        return int(self.nice_lower_gbp * self.gbp_usd_rate)

    @property
    def nice_upper_usd(self) -> int:
        return int(self.nice_upper_gbp * self.gbp_usd_rate)

    @staticmethod
    def _format_rr_range(vals: list[float]) -> str:
        """Format an RR range with enough decimals to show non-null spread.

        Near the null (e.g., cancer/other where all RRs sit in 0.995-1.000),
        two decimals would collapse to '1.00-1.00' and hide the signal.
        Three decimals exposes the spread without making the paper noisy.
        """
        if not vals:
            return ""
        lo, hi = min(vals), max(vals)
        # If either bound rounds differently at 3dp vs 2dp, use 3dp.
        if round(lo, 2) == round(hi, 2):
            return f"{lo:.3f}-{hi:.3f}"
        return f"{lo:.2f}-{hi:.2f}"

    @property
    def cvd_effect_range(self) -> str:
        return self._format_rr_range([p.cvd for p in self.pathway_rrs.values()])

    @property
    def cancer_effect_range(self) -> str:
        return self._format_rr_range([p.cancer for p in self.pathway_rrs.values()])

    @property
    def other_effect_range(self) -> str:
        return self._format_rr_range([p.other for p in self.pathway_rrs.values()])

    @property
    def life_years_range(self) -> str:
        vals = [n.life_years for n in self.nuts.values()]
        return f"{min(vals):.2f}-{max(vals):.2f}"

    @property
    def months_range(self) -> str:
        vals = [n.life_years * 12 for n in self.nuts.values()]
        return f"{min(vals):.1f}-{max(vals):.1f}"

    # Table generators
    def table_3_qalys(self) -> str:
        headers = ["Nut", "Life Years", "Months", "QALY (0% health discount)", "P(>0)", "P(<0)", "ICER"]
        rows = []
        sorted_nuts = sorted(self.nuts.values(), key=lambda n: n.life_years, reverse=True)
        for n in sorted_nuts:
            rows.append([
                n.name, f"{n.life_years:.2f}", f"{n.months:.1f}",
                f"{n.qaly_mean:.2f}",
                f"{n.p_positive:.0%}", f"{n.p_negative:.0%}", f"${n.icer:,.0f}",
            ])
        return _html_table(headers, rows)

    def table_4_pathway_rrs(self) -> str:
        headers = ["Nut", "CVD RR", "Cancer RR", "Other RR"]
        rows = []
        sorted_rrs = sorted(self.pathway_rrs.values(), key=lambda p: p.cvd)
        # Pick per-column precision: use 3 decimals when 2dp would collapse
        # every cell in a column to the same rounded value (happens for
        # cancer/other under the skeptical prior).
        cvd_vals = [p.cvd for p in sorted_rrs]
        cancer_vals = [p.cancer for p in sorted_rrs]
        other_vals = [p.other for p in sorted_rrs]

        def precision(vals: list[float]) -> int:
            if not vals:
                return 2
            rounded = {round(v, 2) for v in vals}
            return 3 if len(rounded) == 1 else 2

        cvd_p = precision(cvd_vals)
        cancer_p = precision(cancer_vals)
        other_p = precision(other_vals)
        for p in sorted_rrs:
            rows.append([
                p.name,
                f"{p.cvd:.{cvd_p}f}",
                f"{p.cancer:.{cancer_p}f}",
                f"{p.other:.{other_p}f}",
            ])
        return _html_table(headers, rows)

    def table_7_sensitivity(self) -> str:
        """Sensitivity of walnut and peanut QALYs to the confounding prior.

        Values are computed live against the current model so the row for
        the base-case prior always matches the headline numbers.
        """
        headers = ["Prior", "Mean", "Interpretation", "Walnut QALY", "Peanut QALY"]
        scenarios = [
            (1.0, 9.0, "Very skeptical"),
            (self.confounding_alpha, self.confounding_beta, "Base case"),
            (1.5, 4.5, "Moderate"),
            (2.5, 5.0, "Optimistic"),
        ]
        # Lazy import avoids circularity at module load
        from whatnut.pipeline import run_analysis

        rows = []
        for alpha, beta, label in scenarios:
            res = run_analysis(
                n_samples=self.n_samples,
                seed=self.seed,
                confounding_alpha=alpha,
                confounding_beta=beta,
            )
            mean_pct = int(round(100 * alpha / (alpha + beta)))
            walnut_q = res.nuts["walnut"].qaly_mean
            peanut_q = res.nuts["peanut"].qaly_mean
            is_base = label == "Base case"
            prior_cell = f"Beta({alpha:g}, {beta:g})"
            if is_base:
                prior_cell = f"<strong>{prior_cell}</strong>"
                label = f"<strong>{label}</strong>"
                mean_pct_cell = f"<strong>{mean_pct}%</strong>"
                walnut_cell = f"<strong>{walnut_q:.2f}</strong>"
                peanut_cell = f"<strong>{peanut_q:.2f}</strong>"
            else:
                mean_pct_cell = f"{mean_pct}%"
                walnut_cell = f"{walnut_q:.2f}"
                peanut_cell = f"{peanut_q:.2f}"
            rows.append([prior_cell, mean_pct_cell, label, walnut_cell, peanut_cell])
        return _html_table(headers, rows)


def _html_table(headers: list[str], rows: list[list[str]]) -> str:
    """Build an HTML table string from headers and rows."""
    ths = "".join(f"<th>{h}</th>" for h in headers)
    body = ""
    for row in rows:
        tds = "".join(f"<td>{c}</td>" for c in row)
        body += f"<tr>{tds}</tr>"
    return f"<table><thead><tr>{ths}</tr></thead><tbody>{body}</tbody></table>"


def _load_results() -> PaperResults:
    """Load results from generated JSON."""
    if not RESULTS_PATH.exists():
        raise FileNotFoundError(
            f"Results file not found: {RESULTS_PATH}\n"
            "Run: python -m whatnut.pipeline --generate"
        )

    with open(RESULTS_PATH) as f:
        data = json.load(f)

    nuts = {}
    pathway_rrs = {}
    for nid, nd in data["nuts"].items():
        nuts[nid] = NutResult(
            name=nd["name"],
            evidence=nd["evidence"],
            qaly_mean=nd["qaly_mean"],
            qaly_ci_lower=nd["qaly_ci_lower"],
            qaly_ci_upper=nd["qaly_ci_upper"],
            qaly_undiscounted_mean=nd["qaly_undiscounted_mean"],
            p_positive=nd["p_positive"],
            p_negative=nd.get("p_negative", round(1 - nd["p_positive"], 2)),
            life_years=nd["life_years_mean"],
            life_years_ci_lower=nd["life_years_ci_lower"],
            life_years_ci_upper=nd["life_years_ci_upper"],
            icer=nd["icer_median"],
            icer_ci_lower=nd["icer_ci_lower"],
            icer_ci_upper=nd["icer_ci_upper"],
            annual_cost=nd["annual_cost"],
            rr_cvd=nd["rr_cvd"],
            rr_cancer=nd["rr_cancer"],
            rr_other=nd["rr_other"],
            cvd_contribution=nd["cvd_contribution"],
            cancer_contribution=nd["cancer_contribution"],
            other_contribution=nd["other_contribution"],
        )
        pathway_rrs[nid] = PathwayRR(
            name=nd["name"],
            cvd=nd["rr_cvd"],
            cancer=nd["rr_cancer"],
            other=nd["rr_other"],
        )

    # Aggregate pathway contributions (integer percent)
    cvd_pct = round(float(data["cvd_contribution_mean"]) * 100)
    cancer_pct = round(float(data["cancer_contribution_mean"]) * 100)
    other_pct = round(float(data["other_contribution_mean"]) * 100)

    # Narrative constants (target_age, NICE thresholds, etc.) are piped
    # through the JSON; fall back to defaults if the JSON is from an older
    # run that predates constants.yaml.
    constants = data.get("constants", {})

    return PaperResults(
        seed=data["seed"],
        n_samples=data["n_samples"],
        start_age=data["start_age"],
        qaly_discount_rate=data["qaly_discount_rate"],
        cost_discount_rate=data["cost_discount_rate"],
        confounding_alpha=data["confounding_alpha"],
        confounding_beta=data["confounding_beta"],
        confounding_mean=data["confounding_mean"],
        confounding_ci_lower=data["confounding_ci_lower"],
        confounding_ci_upper=data["confounding_ci_upper"],
        baseline_life_years=data["baseline_life_years"],
        baseline_qalys=data["baseline_qalys"],
        average_quality_weight=data["average_quality_weight"],
        e_value=data["e_value"],
        cvd_contribution=cvd_pct,
        cancer_contribution=cancer_pct,
        other_contribution=other_pct,
        target_age=constants.get("target_age", 40),
        life_expectancy=constants.get("life_expectancy_approx", 40),
        allergy_prevalence_lower=constants.get("allergy_prevalence_lower", 2.0),
        allergy_prevalence_upper=constants.get("allergy_prevalence_upper", 4.0),
        nice_lower_gbp=constants.get("nice_lower_gbp", 25000),
        nice_upper_gbp=constants.get("nice_upper_gbp", 35000),
        gbp_usd_rate=constants.get("gbp_usd_rate", 1.34),
        icer_threshold=constants.get("icer_threshold_usd", 50000),
        nuts=nuts,
        pathway_rrs=pathway_rrs,
    )


# Module-level singleton — loaded on first import
# Lazy-loaded to avoid errors when results.json doesn't exist yet
_RESULTS: PaperResults | None = None


def get_results() -> PaperResults:
    """Get the loaded results (lazy singleton)."""
    global _RESULTS
    if _RESULTS is None:
        _RESULTS = _load_results()
    return _RESULTS


# For backward compatibility with paper imports: `from whatnut.results import r`
class _LazyR:
    """Lazy proxy so `r = RESULTS` doesn't fail if results.json missing."""

    def __getattr__(self, name):
        return getattr(get_results(), name)


r = _LazyR()
RESULTS = r
